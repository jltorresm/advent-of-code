// 0 - lost
// 3 - draw
// 6 - won

function get_score($input) {
    $total_score = 0;

    $loosers = ["A" => "C", "B" => "A", "C" => "B"];
    $winners = ["A" => "B", "B" => "C", "C" => "A"];

    $choice_score = ["A" => 1, "B" => 2, "C" => 3];
    $win_score = 6;
    $draw_score = 3;
    $loose_score = 0;

    foreach (explode("\n", $input) as $game) {
        $game = str_replace(" ", "", $game);

        switch ($game[1]) {
            // Loose
            case 'X':
                $total_score += $loose_score + $choice_score[$loosers[$game[0]]];
                break;

            // Draw
            case 'Y':
                $total_score += $draw_score + $choice_score[$game[0]];
                break;

            // Win
            case 'Z':
                $total_score += $win_score + $choice_score[$winners[$game[0]]];
                break;
        }
    }

    return $total_score;
}

